// Create a program to change the value of a variable.
// Create an integer variable number with value 31.
// Print the variable.
// Change the value of the variable to 99.
// Print the variable again in the next line.

#include <stdio.h>

int main() {
  
    // create and print an integer variable number with value 31
    int number =31;
    printf("%d\n",number);

    // change the value of variable to 99
    number=99;


    // print the variable in the next line
    printf("%d",number);

    return 0;
}